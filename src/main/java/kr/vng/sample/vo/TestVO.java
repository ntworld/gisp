package kr.vng.sample.vo;

public class TestVO {

	private String id;
	private String place_name;
	private String address;
	private String facility_phone;
	private float x_coord;
	private float y_coord;
	
	private String category;

	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPlace_name() {
		return place_name;
	}
	public void setPlace_name(String place_name) {
		this.place_name = place_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getFacility_phone() {
		return facility_phone;
	}
	public void setFacility_phone(String facility_phone) {
		this.facility_phone = facility_phone;
	}
	public float getX_coord() {
		return x_coord;
	}
	public void setX_coord(float x_coord) {
		this.x_coord = x_coord;
	}
	public float getY_coord() {
		return y_coord;
	}
	public void setY_coord(float y_coord) {
		this.y_coord = y_coord;
	}

	
}
